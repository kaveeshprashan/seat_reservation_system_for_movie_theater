    ObservableList<Button> list = FXCollections.observableArrayList();


    public void ButtonClicked(ActionEvent event) {


        if (((Button) event.getTarget()).getStyle() == "-fx-background-color:slateGrey") {
            ((Button) event.getTarget()).setStyle("-fx-background-color:lime");

            if (parseInt(((Button) event.getTarget()).getId()) > 78) {
                total.setText(String.valueOf(parseInt(total.getText()) + 20));
                seats.setText(String.valueOf(parseInt(seats.getText()) - 1));
                tickets.setText(String.valueOf(parseInt(tickets.getText()) + 1));
            } else {
                total.setText(String.valueOf(parseInt(total.getText()) + 12));
                seats.setText(String.valueOf(parseInt(seats.getText()) - 1));
                tickets.setText(String.valueOf(parseInt(tickets.getText()) + 1));
            }

            list.add(((Button) event.getSource()));

        } else if (((Button) event.getTarget()).getStyle() == "-fx-background-color:lime") {
            ((Button) event.getTarget()).setStyle("-fx-background-color:slateGrey");
            if (parseInt(((Button) event.getTarget()).getId()) > 78) {
                total.setText(String.valueOf(parseInt(total.getText()) - 20));
                seats.setText(String.valueOf(parseInt(seats.getText()) + 1));
                tickets.setText(String.valueOf(parseInt(tickets.getText()) - 1));
            } else {
                total.setText(String.valueOf(parseInt(total.getText()) - 12));
                seats.setText(String.valueOf(parseInt(seats.getText()) - 1));
                tickets.setText(String.valueOf(parseInt(tickets.getText()) - 1));
            }

            list.remove(((Button) event.getTarget()));

        }

    }

    public void BookNowButtonClicked(ActionEvent event) {


        for (Button button : list) {

            button.setDisable(true);
            button.setStyle("-fx-disabled-opacity: 1.0");
            button.setStyle("-fx-background-color:red");


        }

        total.setText("0");
        tickets.setText("0");

    }
